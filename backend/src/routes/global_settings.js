/**
 *  @swagger
 *  /api/global_settings:
 *    get:
 *      security:
 *        - bearerAuth: []
 *      tags: [Global_settings]
 *      summary: Get all global_settings
 *      description: Get all global_settings
 *      responses:
 *        200:
 *          description: Global_settings list successfully received
 *          content:
 *            application/json:
 *              schema:
 *                type: array
 *                items:
 *                  $ref: "#/components/schemas/Global_settings"
 *        401:
 *          $ref: "#/components/responses/UnauthorizedError"
 *        404:
 *          description: Data not found
 *        500:
 *          description: Some server error
 */

router.get(
  '/',
  wrapAsync(async (req, res) => {
    const filetype = req.query.filetype;
    const payload = await Global_settingsDBApi.findAll(req.query);
    if (filetype && filetype === 'csv') {
      const fields = [
        'id',
        'name',
        'subscription_expiry_notification_days',
        'billing_cycle_grace_period',
      ];
      const opts = { fields };
      try {
        const csv = parse(payload.rows, opts);
        res.status(200).attachment(csv);
        res.send(csv);
      } catch (err) {
        console.error(err);
      }
    } else {
      res.status(200).send(payload);
    }
  }),
);

